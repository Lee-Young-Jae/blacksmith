import type { EquipmentBase, EquipmentLevel } from "../../types/equipment";

function createLevels(
  equipmentId: string,
  levelData: Omit<EquipmentLevel, "image">[]
): EquipmentLevel[] {
  return levelData.map((data, index) => ({
    ...data,
    image: `/equipment/${equipmentId}/${index}.png`,
  }));
}

